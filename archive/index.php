	   <p>Dear Friends and Colleagues,</p>
	   <p>In 2012, we faced some of the nation’s greatest challenges as we confronted increasingly complex questions around the future of health care and the impact to the economy, environmental quality, the safety and well-being of individuals and families, and the health of our communities. At Kaiser Permanente, we have never taken a passive role in helping to create positive change for the underserved.  For more than 65 years, we have worked to transform lives for the better. We have demonstrated that commitment through not only our care and coverage, but by continuing to deepen and support our ties with the communities we serve, with one primary goal in mind – to end disparities in health care.</p>
	   <p>This year we began to lay the groundwork for the new world of health care under the Affordable Care Act, which will go into effect in 2014. While the ACA will provide coverage for millions of people, there will still be gaps in coverage for many.</p>
	   <p>Throughout the year, we worked to reshape key components of our care and coverage programs to meet the needs of the newly insured and the uninsured. We maintained focus on increasing access to care, collaborating with our safety net partners, advocating for behavior changes such as healthier eating and active living, as well as policies that improve the environments where we live, work, learn and play. We continued to share our research findings and the learnings from our care delivery settings.  Through all this, we extended the reach of our programs with grants to and partnerships with thousands of nonprofit organizations across the country.</p>
	   <p>All told, we invested $2 billion to improve the health of our members and others in the communities we serve.</p>
	   <p>As we look ahead to the changes that ACA brings ,  we will remain nimble in an ever changing health care environment, while never losing sight of what matters most – improving total health – mind, body and spirit for our brothers and sisters around the nation and the world.</p>
	   <p>We continue to build upon our mission to deliver high-quality, affordable health care and to improve the health of our members and the communities we serve. In the coming year, we will focus on</p>
	   <ul>
	   	   <li>Activating behavior change both within health care settings and our communities</li>
		   <li>Overhauling our subsidized programs to ensure that the most vulnerable populations can access care when and where they need it</li>
		   <li>Performing comprehensive community health needs assessments to better understand the unique barriers to total health in our communities, and</li>
		   <li>Deepening partnerships with the safety net and other nonprofit organizations to better address the social, economic, and environmental factors that influence healthy living</li>
		   <li>Supporting critical work in communities and around the nation.</li>
	   </ul>
	   <p>In committing to support the Total Health, we are examining the ways in which we can fully support the health of our members and the communities we serve. We are deepening partnerships with schools and expanding efforts to help our 270,000 employees and their families make healthy choices.</p>
	   <p>We hope this report will give you a snapshot of some of the important work occurring across the nation and inspire you to make a difference in your community. Continue to follow Kaiser Permanente Community Benefit work on the web at http://share.kaiserpermanente.org/.</p>
